package jdk_proxy.impl;

import jdk_proxy.MathUtil;

public class MathUtilImpl implements MathUtil {

    public int add(int a, int b) {
        return a + b;
    }

    public int sub(int a, int b) {
        return a - b;
    }

    public int mul(int a, int b) {
        return a * b;
    }

    public int div(int a, int b) {
        return a / b;
    }
}
